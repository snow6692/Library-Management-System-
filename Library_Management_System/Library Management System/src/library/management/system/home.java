    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String userName = jTextField1.getText();
    String email = jTextField2.getText();
    String pass = jTextField3.getText();

    // Validate input fields
    if (userName.isEmpty() || email.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(null, "All fields are required.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Create UserActionContext object
    UserActionContext context = new UserActionContext();

    // Set the strategy to Registration
    context.setStrategy(new RegisterStrategy());

    // Execute the registration strategy
    context.executeStrategy(userName, email, pass, this);
        
    }//GEN-LAST:event_jButton1ActionPerformed

