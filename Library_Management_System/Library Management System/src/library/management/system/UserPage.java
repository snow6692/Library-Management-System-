    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    int selectedRow = jTable1.getSelectedRow();  

   
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a row from the table first!", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }


    int bookId = (int) jTable1.getValueAt(selectedRow, 0);
    String bookName = (String) jTable1.getValueAt(selectedRow, 1);
    String author = (String) jTable1.getValueAt(selectedRow, 3);
    int copiesCount = (int) jTable1.getValueAt(selectedRow, 2);

String borrowerEmail = jTextField1.getText().trim();
if(borrowerEmail.isEmpty()){
    JOptionPane.showMessageDialog(null, "Email is Empty !", "Error", JOptionPane.ERROR_MESSAGE);
    return; 
}

    UserDAO userDAO = new UserDAO();

    if (!userDAO.isEmailExist(borrowerEmail)) {
        JOptionPane.showMessageDialog(null, "Email not found in the system!", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }


    if (copiesCount <= 0) {
        JOptionPane.showMessageDialog(null, "Not enough copies of the book!", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }

    OperationContext context = new OperationContext();


    OperationState updateCopiesState = new UpdateBookCopiesState(bookId);
    context.setState(updateCopiesState);
    context.execute();  

    
    OperationState addLoanState = new AddLoanRecordState(bookId, bookName,author, borrowerEmail);
    context.setState(addLoanState);  
    context.execute();  


    JOptionPane.showMessageDialog(null, "Book borrowed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    displayBooks();
    }//GEN-LAST:event_jButton1ActionPerformed

