    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String bookName = jTextField1.getText();
    String copiesCountStr = jTextField2.getText();
    String author = jTextField3.getText();
    
    // Validate the input
    if (bookName.isEmpty() || author.isEmpty() || copiesCountStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "All fields are required!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Validate if copies count is a valid number
    int copiesCount;
    try {
        copiesCount = Integer.parseInt(copiesCountStr);
        if (copiesCount <= 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "Copies count must be greater than 0.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Copies count must be a valid number.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Create a Book object with the input
    Book newBook = new Book(bookName, author, copiesCount);
    
    // Create a command to add the book
    Command addBookCommand = new AddBookCommand(newBook);
    
    // Create an Invoker object and execute the command
    Invoker invoker = new Invoker();
    invoker.setCommand(addBookCommand);
 invoker.executeCommand();
    
    // Show success message
    javax.swing.JOptionPane.showMessageDialog(null, "Book added successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
  jTextField1.setText("");
    jTextField2.setText("");
      jTextField3.setText("");
              displayBooks();
              
                 
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow != -1) {  // Check if a row is selected
        // Get the book ID from the table (assuming the first column is the book_id)
        int bookId = (int) jTable1.getValueAt(selectedRow, 0);

        // Create a command to delete the book by ID
        Command deleteBookCommand = new DeleteBookCommand(bookId);
        
        // Create an Invoker object and execute the command
        Invoker invoker = new Invoker();
        invoker.setCommand(deleteBookCommand);
        invoker.executeCommand();

        // Show success message
        javax.swing.JOptionPane.showMessageDialog(this, "Book deleted successfully.");

        // Remove the row from the table (GUI)
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.removeRow(selectedRow);
        
        // Display updated books in the table
        displayBooks();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a book to delete.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

