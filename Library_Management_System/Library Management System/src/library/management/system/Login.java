    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String email = jTextField1.getText();
    String pass = jTextField2.getText();

    // Validate input fields
    if (email.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Email or Password cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Create UserActionContext object
    UserActionContext context = new UserActionContext();

    // Set the strategy to Login
    context.setStrategy((UserActionStrategy) new LoginStrategy());

    // Execute the login strategy
    context.executeStrategy(null, email, pass, this);
       
    }//GEN-LAST:event_jButton1ActionPerformed

